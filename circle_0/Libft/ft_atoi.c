#include "libft.h"

int	ft_atoi(const char *str)
{
	int		i;
	int		num;
	char	sign;

	sign = '+';
	i = 0;
	num = 0;
	while (str[i] == ' ' || (str[i] >= 9 && str[i] <= 13))
		i++;
	if (str[i] == '-')
	{
		sign = '-';
		i++;
	}
	else if (str[i] == '+')
		i++;
	while (str[i] >= '0' && str[i] <= '9')
	{
		num = (num * 10) + str[i] - '0';
		i++;
	}
	if (sign == '-')
		num = -num;
	return (num);
}
