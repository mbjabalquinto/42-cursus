#include "ft_printf.h"

int	ft_putstr(char *str)
{
	int	i;

	i = 0;
	if (!str)
		str = "(null)";
	while (str[i] != '\0')
	{
		if (write(1, &str[i], 1) == -1)	
			return (-1);
		i++;
	}
	return (i);
}

